interface DottedTextProps {
  text: string
  dotSize?: number
  gap?: number
  color?: string
  accentColor?: string
  accentIndices?: number[]
  className?: string
  animate?: boolean
}

export function DottedText({ 
  text, 
  dotSize = 4, 
  gap = 2, 
  color = "#f1f1f1",
  accentColor = "#d71921",
  accentIndices = [],
  className = "",
  animate = true
}: DottedTextProps) {
  const chars = text.toUpperCase().split('')
  const charWidth = 5 * dotSize + 4 * gap
  const charHeight = 7 * dotSize + 6 * gap
  const charSpacing = dotSize * 2
  
  const totalWidth = chars.length * charWidth + (chars.length - 1) * charSpacing
  
  return (
    <div 
      className={`inline-flex ${className}`}
      role="img"
      aria-label={text}
    >
      <svg 
        width={totalWidth} 
        height={charHeight}
        viewBox={`0 0 ${totalWidth} ${charHeight}`}
      >
        {chars.map((char, charIndex) => {
          const pattern = dotPatterns[char] || dotPatterns[' ']
          const charX = charIndex * (charWidth + charSpacing)
          const isAccent = accentIndices.includes(charIndex)
          const dotColor = isAccent ? accentColor : color
          
          return pattern.map((row, rowIndex) => 
            row.map((dot, colIndex) => {
              if (dot === 0) return null
              
              const x = charX + colIndex * (dotSize + gap)
              const y = rowIndex * (dotSize + gap)
              const delay = animate ? (charIndex * 0.05 + (rowIndex * 5 + colIndex) * 0.01) : 0
              
              return (
                <motion.circle
                  key={`${charIndex}-${rowIndex}-${colIndex}`}
                  cx={x + dotSize / 2}
                  cy={y + dotSize / 2}
                  r={dotSize / 2}
                  fill={dotColor}
                  initial={animate ? { opacity: 0, scale: 0 } : { opacity: 1, scale: 1 }}
                  animate={{ opacity: 1, scale: 1 }}
                  transition={{ 
                    duration: 0.3, 
                    delay,
                    ease: "easeOut"
                  }}
                />
              )
            })
          )
        })}
      </svg>
    </div>
  )
}

// Compact version for smaller displays
export function DottedNumber({ 
  value, 
  label,
  dotSize = 3,
  className = ""
}: { 
  value: string
  label?: string
  dotSize?: number
  className?: string
}) {
  return (
    <div className={`flex flex-col items-center ${className}`}>
      <DottedText 
        text={value} 
        dotSize={dotSize} 
        gap={1}
        accentIndices={[0]}
        animate={true}
      />
      {label && (
        <span className="text-xs font-mono text-muted-foreground mt-2 uppercase tracking-wider">
          {label}
        </span>
      )}
    </div>
  )
}

// Decorative dotted divider
export function DottedDivider({ className = "" }: { className?: string }) {
  return (
    <div className={`flex items-center justify-center gap-2 ${className}`}>
      {Array.from({ length: 5 }).map((_, i) => (
        <motion.div
          key={i}
          className="w-2 h-2 rounded-full bg-accent"
          initial={{ opacity: 0, scale: 0 }}
          animate={{ opacity: 1, scale: 1 }}
          transition={{ delay: i * 0.1 }}
        />
      ))}
    </div>
  )
}
